// 5、求1!+2!+3!+...+20!的和
// 步骤
// 1. 声明一个变量 累加
var sum = 0;
// 2. 循环1-20之间的数
for (var i = 1; i <= 20; i++) {
    // 3. 声明一个变量 保存阶乘
    var tmp = 1;
    // 4. 循环i之前的数  就是阶乘的各个乘数
    for (var t = 1; t <= i; t++) {
        // 4.1 相乘 
        tmp *= t;
    }
    // 5. 累加
    sum += tmp;
}
// 6. 打印
console.log("结果是:" + sum);