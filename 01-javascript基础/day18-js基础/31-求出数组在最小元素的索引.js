// 31.求出数组var arr= [1,2,4,5,10,100,2,-22]中最小元素的索引

// 步骤
// 1. 声明一个数组
var arr = [1, 2, 4, 5, 10, 100, 2, -22];
// 2. 声明一个遍历 假设一个最小值
var min = arr[0];
// 3. 声明一个变量 保存索引
var num=0;
// 4. 循环遍历数组
for (var i=0; i < arr.length; i++) {
    // 5. 判断如果数组中有值比最小值小  就把那个值赋值给最小值
    if (arr[i] < min) {
        min = arr[i];
        // 5.1 让num等于下标
        num=i;
    }
}
console.log('最小的元素是:'+min+'它的索引是'+num);