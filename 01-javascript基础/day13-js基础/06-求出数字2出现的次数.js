// 6.  var arr = [1,1,2,2,3,4,5,4,3,2,1], 打印出数字2出现的次数。
// 步骤
// 1.声明一个数组
var arr = [1, 1, 2, 2, 3, 4, 5, 4, 3, 2, 1];
// 2. 声明一个变量  保存2出现的次数
var num = 0;
// 3. 用循环遍历数组
for (var i = 0; i < arr.length; i++) {
    // 4. 判断数组中的每个值是否等于2
    if (arr[i] == 2) {
        // 4.1 如果等于2 num就+1
        num += 1;
    }
}
// 5. 打印
console.log('数字2出现的次数为' + num + '次');